# This program shows how to use len() [length] with strings
# len(object)

# Define a text string
message = "Hello World"

# Index positions:
# H -> 0, e -> 1, l -> 2, l -> 3, o -> 4
# space -> 5
# W -> 6, o -> 7, r -> 8, l -> 9, d -> 10

# len() returns the total number of characters
length = len(message)

# Printing result
print(f"The length of the string is: {length}") # It prints 11 because It starts with 1, no with 0
