# This program shows an example of triple quotes
# It's ideal for long text like menus or letters

# Example 1: restaurant menu

menu = """
MENU OF THE DAY
---------------
1. Tacos al pastor
2. Enchiladas verdes
3. Quesadillas
"""

print(menu)

# Example 2: E-mail writing

email = """
Hello, team:

I want you to know that I just finished my python homework.
The code works perfectly!

Best regards.
"""

print(email)