"""
While Loop Example

This program demonstrates how a while loop works
by printing numbers from 1 to 5.

Author: Lalo Tellez
"""

print(f'{"=" * 20} While Loop Example {"=" * 20}\n')
print('Printing values from 1 to 5:')

# Initialize counter
counter = 1

while counter <= 5:
    print(counter, end=' ')
    counter += 1  # Increment counter

print(f'\n{"=" * 50}')
